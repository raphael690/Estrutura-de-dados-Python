
dados = {"nome": "Guilherme", "idade": "28", "telefone": "3333-1234"}

print(dados["nome"]) # Guilherme
print(dados["idade"]) # 28
print(dados["telefone"]) #3333-1234

dados["nome"] = "Maria" # mudando o nome para maria
dados["idade"] = "18" # mudando a idade para 18
dados["telefone"] = "9988-1781" # mudando o numero de telefone

print(dados) # {"nome":"Maria", "idade":18, "telefone": "9988-1781"}